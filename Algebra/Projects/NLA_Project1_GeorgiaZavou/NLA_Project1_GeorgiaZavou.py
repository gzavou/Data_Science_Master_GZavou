#Compute the step size for the Newton method to ensure feasibility of Lagrange multipliers and slack variables.

    #Parameters
    #lamb0: Current values of Lagrange multipliers
    #lamb1: changes to the Lagrange multipliers
    #s0: Current values of slack variables
    #ds:  changes to the slack variables


def Newton_step(lamb0, lamb1, s0, ds):

    alpha = 1
    index_lamb0 = np.array(np.where(lamb1 < 0))

    if index_lamb0.size > 0:

        alpha = min(alpha,np.min(-lamb0[index_lamb0]/lamb1[index_lamb0]))
    index_s0 = np.array(np.where(ds<0))

    if index_s0.size > 0:

        alpha = min(alpha, np.min(-s0[index_s0]/ds[index_s0]))

    return alpha


def Matrix_KKT(G, C, n, m, lamb, s):


    #Parameters
    #G : Coefficients for the quadratic objective function.
    #C : Constraint matrix.
    #n : Number of variables.
    #m  Number of constraints.
    #lamb : Lagrange multipliers.
    #s : Slack variables.

    S = np.diag(s)
    Lambdas = np.diag(lamb)
    eq1 = np.concatenate((G, -C, np.zeros((n, m))), axis = 1)
    eq2 = np.concatenate((np.transpose(-C), np.zeros((m, m)), np.identity(m)), axis = 1)
    eq3 = np.concatenate((np.zeros((m, n)), S, Lambdas), axis = 1)
    Mat = np.concatenate((eq1, eq2, eq3))

    #Returning Variables:
    #The KKT matrix.
    #The diagonal matrix of slack variables (S).
    #The diagonal matrix of Lagrange multipliers (Lambdas)

    return Mat, S, Lambdas

def F(x, G, g):

    #The return value is the value of the objective function.
    return 0.5 * np.transpose(x).dot(G).dot(x) + np.transpose(g).dot(x)


def F_z(x,lamb,s, G, g, C, d):

    equasion1 = G.dot(x) + g - C.dot(lamb)
    equasion2 = s + d - np.transpose(C).dot(x)
    equasion3 = s * lamb

    #Return value is a vector of equations for the KKT conditions.
    return np.concatenate((equasion1, equasion2, equasion3))


def Program_for_C2(n, maxIter=100, epsilon=10e-16):

  #Parameters
  #n: The number of variables in the optimization problem
  #maxIter: The maximum number of iterations allowed for the algorithm
  #epsilon: Threshold for convergence
  #Print_Time: Option to print computation time
  #Print_Results: Option to print optimization results

    #random number generation
    np.random.seed(2)
    random.seed(2)

    #Initialize our variables
    m = 2*n  # m is twice as the number of variables
    x = np.zeros((n)) # initial decision variables zero vector
    lamb = np.ones((m)) # lagrange multiplier vector
    s = np.ones((m)) # slack variables
    z = np.concatenate((x, lamb, s)) # vector of z
    G = np.identity(n)       # G is the identity matrxi for the objective function
    C = np.concatenate((G, - G), axis = 1) # inequality constrains
    d = np.full((m), - 10)
    e = np.ones((m))
    g = np.random.normal(0, 1, (n))

    Mat, S, Lambdas = Matrix_KKT(G, C, n, m, lamb,s)

    for i in range(maxIter):


        #The KKT conditions are evaluated, and the system is solved for the update vector delta.
        b = -F_z(x, lamb, s, G, g, C, d)
        delta = np.linalg.solve(Mat,b)

        #Get the value for alpha using predefined Newton step
        alpha = Newton_step(lamb, delta[n:n+m], s, delta[n+m:])

        # Step Size Adjustment
        mu = s.dot(lamb) / m
        Mu = ((s + alpha * delta[n+m:]).dot(lamb + alpha * delta[n:(n + m)])) / m
        sigma = (Mu/mu)**3

        # Correcting sub-step
        b[(n + m):] = b[(n + m):] - np.diag(delta[(n + m):]*delta[n:(n + m)]).dot(e) + sigma * mu * e

        # We get delta by using the linalg function
        delta = np.linalg.solve(Mat, b)

        #  Step-size correction with delta
        alpha = Newton_step(lamb, delta[n:(n + m)], s, delta[(n + m):])

        # update the sub-step
        z = z + (alpha * delta) * 0.95

        # Define the Stopping citeria for Convergence
        if (np.linalg.norm(-b[:n]) < epsilon) or (np.linalg.norm(-b[n:(n + m)]) < epsilon) or (np.abs(mu) < epsilon):
            break

        # Update our variables for next iteration
        x = z[:n]
        lamb = z[n:(n + m)]
        s = z[(n + m):]
        Mat, S, Lambdas = Matrix_KKT(G, C, n, m, lamb, s)

    print('Minimum from  the function:', F(x, G, g))
    print('Real minimum is:', F(-g, G, g))
    print('Iterations:', i)
    print('Condition number:', np.linalg.cond(Mat))

    return(i, abs(F(x, G, g) - F(-g, G, g)), np.linalg.cond(Mat))

