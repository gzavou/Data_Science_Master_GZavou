import numpy as np

def ReadMatrix(source, shape, symm=False):

    matrix = np.zeros(shape)

    with open(source, "r") as file:

        a = file.readlines()

    for line in a:

        row, column, value = line.strip().split()
        row = int(row)
        column = int(column)
        value = float(value)
        matrix[row - 1, column - 1] = value

        if symm == True:

            matrix[column - 1, row - 1] = value

    return matrix

# Function to read a vector from a file
def ReadVector(source, n):

    # Parameters:
    # source  Path to the input file containing the vector data
    #n The number of elements in the vector

    # Initialize an empty vector
    v = np.zeros(n)

    # Open the file containing the vector data
    with open(source, "r") as file:
        # Read all lines from the file
        lines = file.readlines()

    # Iterate over each line in the file
    for line in lines:
        # Split the line by spaces to extract the index and value
        idx, value = line.strip().split()

        # Convert the index to an integer (1-based to 0-based indexing)
        idx = int(idx)

        # Convert the value to a float
        value = float(value)

        # Fill the vector with the value at the specified index (idx-1 for 0-based indexing)
        v[idx - 1] = value

    # Return the populated vector
    return v



